from fpdf import FPDF


def test_case():

    pdf = FPDF()
    pdf.add_page()
    pdf.set_font("Helvetica")
    pdf.code39("*1*", x=0, y=10, w=1, h=5)
    pdf.code39("*2*", x=0, y=20, w=1, h=5)
    pdf.code39("*3*", x=0, y=30, w=1, h=5)
    pdf.code39("*4*", x=0, y=40, w=1, h=5)
    pdf.code39("*5*", x=0, y=50, w=1, h=5)
    pdf.code39("*6*", x=0, y=60, w=1, h=5)
    pdf.code39("*7*", x=0, y=70, w=1, h=5)
    pdf.code39("*8*", x=0, y=80, w=1, h=5)
    pdf.code39("*9*", x=0, y=90, w=1, h=5)
    pdf.code39("*A*", x=0, y=100, w=1, h=5)
    pdf.code39("*Z*", x=0, y=110, w=1, h=5)
    pdf.set_xy(0, 0)
    pdf.cell(5.33 * 3, 2000, border=1)
    pdf.code39("*1111111111111*", x=0, y=200, w=1, h=5)
    pdf.code39("*ZZZZZZZZZZZZZ*", x=0, y=210, w=1, h=5)
    pdf.set_xy(0, 0)
    pdf.cell(5.33 * 18, 2000, border=1)
    pdf.code39("*11111111111111111111111111111111*", x=0, y=240, w=1, h=5)
    pdf.code39("*ZZZZZZZZZZZZZZZZZZZZZZZZZZZZZZZZ*", x=0, y=250, w=1, h=5)
    pdf.set_xy(0, 0)
    pdf.cell(5.33 * 34, 2000, border=1)
    pdf.output("result.pdf")
